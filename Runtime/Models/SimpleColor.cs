namespace BricksBucket.Web.Zeplin.Models
{
    /// <summary>
    /// Simple representation of a color.
    /// </summary>
    [System.Serializable]
    public struct SimpleColor
    {
        /// <summary>
        /// Red component.
        /// </summary>
        public int r;

        /// <summary>
        /// Green component.
        /// </summary>
        public int g;

        /// <summary>
        /// Blue component.
        /// </summary>
        public int b;

        /// <summary>
        /// Alpha component.
        /// </summary>
        public float a;
    }
}